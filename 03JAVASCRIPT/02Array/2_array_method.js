let arr = [10, 11, 12, 13];

console.log(arr);

// Last
// to add new element at the last 
arr.push(8);
arr.push(14);
console.log(arr);

// to remove an element from the last 
arr.pop();
arr.pop();
console.log(arr);

// Start
// to remove an element from the start 
arr.shift();

// to add a new element at the start 
arr.unshift(80);

console.log(arr);



 


